void binprintf(int IP)											//==============PRINT AS BINARY================
{
    unsigned int aux=1<<((sizeof(int)<<3)-1);					//Change the pointer
    while(aux) {												//While aux is true
        printf("%d", (IP&aux ? 1 : 0));							//Print it
        aux >>= 1;												//Move it baby
    }
}

bool Analizer(byte T[], int i){
	sint ToT=0;

	ToT=ToT|T[12]<<8;



	ToT|=T[13];
	sprintf(resultado+strlen(resultado),"\n\n Trama %-3d ",i+1);
	sprintf(resultado+strlen(resultado)," ToT %-4d ",ToT);

	if(ToT<1500){
		sprintf(resultado+strlen(resultado),"\n##################Protocolo LLC####################\n ");
		//sprintf(resultado+strlen(resultado),"Tipo %d ",T[C1]&0b11);
		switch(T[C1]&0b11){
			case 0:
		//	printf("Entre a caso 0\n");
			sprintf(resultado+strlen(resultado),"T-Informacion N(s):%-5dN(r):%-5d ",T[C1]>>1,T[C2]>>1);
			break;

			case 1:
		//	printf("Entre a caso Trama de Supervisi贸n\n");
			//printf("%d\n",(T[C1]>>2)&3 );

			sprintf(resultado+strlen(resultado),"T-Supervisi贸n 		N(R):%-5d %s ",T[C2]>>1,Supervision[(T[C1]>>2)&3]);
			break;

			case 2:
		//	printf("Entre a caso Trama de Informaci贸n\n");
			sprintf(resultado+strlen(resultado),"T-Informaci贸n N(s):%-5dN(r)%-5d ",T[C1]>>1,T[C2]>>1);
			break;

			case 3:
			//printf("Entre caso 3 Unnummered\n");
			if (((T[C1]>>4)&1))
			{
			//	printf("Checa Sap\n");
				if (T[15]&1)
				{
					//printf("Respuesta %d",(T[C1]>>2)&3)|((T[C1]>>3)&28);
					sprintf(resultado+strlen(resultado),"T-Unnumerated Respuesta %15s ",UnumerR[((T[C1]>>2)&3)|((T[C1]>>3)&28)]);
				}
				else if (!(T[15]&1))
				{
					//printf("Comando %d",(T[C1]>>2)&3)|((T[C1]>>3)&28);
					sprintf(resultado+strlen(resultado),"T-Unnumerated Comando 	 %13s ",UnumerC[((T[C1]>>2)&3)|((T[C1]>>3)&28)]);
				}
				
			}
			else sprintf(resultado+strlen(resultado),"T-Unnumerated Sin PF");
			
		

			break;



		}
		return 1;
	}
	else if (ToT==2054)
	{
		sprintf(resultado+strlen(resultado),"\n##################Protocolo ARP####################\n Hardware Type :");
		switch(T[15]){
		case 1:
		sprintf(resultado+strlen(resultado)," Ethernet. ");
		break;
		case 6:
		sprintf(resultado+strlen(resultado)," Token Ring.");

		break;
		case 15:
		sprintf(resultado+strlen(resultado)," Frame Relay. ");

		break;
		case 16:
		sprintf(resultado+strlen(resultado)," ATM. ");
		break;
		}
		sprintf(resultado+strlen(resultado),"Protocol Type: ");
		if(T[16]==8)
			sprintf(resultado+strlen(resultado)," IP. ");

		byte HWl=T[18];
		byte PAl=T[19];

		sprintf(resultado+strlen(resultado)," Operation Code: ");
		switch(T[21]){
			case 1:
			sprintf(resultado+strlen(resultado)," ARP Request. ");

			break;
			case 2:
			sprintf(resultado+strlen(resultado)," ARP Reply.");
			break;
			case 8:
			sprintf(resultado+strlen(resultado)," Inverse ARP Request. ");

			break;

			case 9:
			sprintf(resultado+strlen(resultado)," Inverse ARP Reply. ");

			break;
		}


		sint IP=0;
		sprintf(resultado+strlen(resultado),"\n Sender HW Address: ");

		for (byte i = 22; i < 22+HWl; i++)
		{
			sprintf(resultado+strlen(resultado),"%02x ",T[i]);
		}
		sprintf(resultado+strlen(resultado),"  \n Sender Protocol allow:  ");

		
			IP=CreateIP(&T[22+HWl]);

			sprintf(resultado+strlen(resultado),"%d.%d.%d.%d\n", ((IP >> 24) & 0xFF), ((IP >> 16) & 0xFF), ((IP >> 8) & 0xFF), (IP & 0xFF));
			sprintf(resultado+strlen(resultado),"%u  \n",IP);

		
		sprintf(resultado+strlen(resultado)," Target HW address:  ");

		for (byte i = 22+HWl+PAl; i < 22+HWl+HWl+PAl; i++)
		{
			sprintf(resultado+strlen(resultado),"%02x ",T[i]);
		}
		sprintf(resultado+strlen(resultado),"  \n Target Protocol Address:  ");

	
			IP=CreateIP(&T[22+HWl+PAl+HWl]);
			sprintf(resultado+strlen(resultado),"%d.%d.%d.%d\n", ((IP >> 24) & 0xFF), ((IP >> 16) & 0xFF), ((IP >> 8) & 0xFF), (IP & 0xFF));
			sprintf(resultado+strlen(resultado),"%u  \n\n",IP);
		

		return 1;
		
	}

	else return 1;
}

